package com.seyo.For;

public class Main1 {

	public static void main(String[] args) {
			for (int i=1; i<=5; i++) {
				System.out.println(i);
			}
	}

}


//for문은 반복적인 작업을 수행할 때 사용.
//1부터 5까지의 숫자를 출력, for문을 사용해서 1부터 5까지의 범위를 설정하고, 각 숫자를 출력함.
//for문을 사용해 i를 1부터 5까지 증가시키며 반복함.
//int i =1 :  i를 1로 초기화함.
//i<=5 : i가 5보다 작거나 같을 때까지 반복함.
//i++ : 반복할 때마다 i를 1씩 증가시킴.